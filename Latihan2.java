    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
    double a = Double.parseDouble(txtNilai1.getText());
    double b = Double.parseDouble(txtNilai2.getText());
    double hasil = a + b;

    String nilai1 = (a % 1 == 0) ? String.valueOf((int) a) : String.valueOf(a);
    String nilai2 = (b % 1 == 0) ? String.valueOf((int) b) : String.valueOf(b);
    String output = (hasil % 1 == 0) ? String.valueOf((int) hasil) : String.valueOf(hasil);

    txtHasil.setText(
        "Nilai 1 : " + nilai1 +
        "\nNilai 2 : " + nilai2 +
        "\nProses : Penjumlahan" +
        "\nHasil : " + output
    );
    }//GEN-LAST:event_btnTambahActionPerformed

